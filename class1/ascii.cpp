#include<iostream>
using namespace std;
int main(){
    int a;
    a=12;
    float b=3.23;
    char c;
    cout<<"size of int:"<<sizeof(a)<<endl;
    cout<<"size of float:"<<sizeof(b)<<endl;
    cout<<"size of char:"<<sizeof(c)<<endl;
    bool d;
    cout<<"size of bool:"<<sizeof(d)<<endl;
    short int k;
    short int s;
    cout<<"size of short int:"<<sizeof(k)<<endl;
    cout<<"size of long int:"<<sizeof(s)<<endl;
    return 0;
}
